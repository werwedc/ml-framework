namespace MobileRuntime
{
    /// <summary>
    /// Data types supported by tensors.
    /// </summary>
    public enum DataType
    {
        /// <summary>
        /// 32-bit floating point.
        /// </summary>
        Float32,

        /// <summary>
        /// 16-bit floating point.
        /// </summary>
        Float16,

        /// <summary>
        /// 8-bit signed integer.
        /// </summary>
        Int8,

        /// <summary>
        /// 16-bit signed integer.
        /// </summary>
        Int16,

        /// <summary>
        /// 32-bit signed integer.
        /// </summary>
        Int32
    }
}
